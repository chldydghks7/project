package yjc.wdb.gr.bean;

public class GroupRank {
	private int gr_id;
	private String gr_name;
	private int gr_number;
	private int gr_catch;
	private int gr_avspeed;
	public int getGr_id() {
		return gr_id;
	}
	public void setGr_id(int gr_id) {
		this.gr_id = gr_id;
	}
	public String getGr_name() {
		return gr_name;
	}
	public void setGr_name(String gr_name) {
		this.gr_name = gr_name;
	}
	public int getGr_number() {
		return gr_number;
	}
	public void setGr_number(int gr_number) {
		this.gr_number = gr_number;
	}
	public int getGr_catch() {
		return gr_catch;
	}
	public void setGr_catch(int gr_catch) {
		this.gr_catch = gr_catch;
	}
	public int getGr_avspeed() {
		return gr_avspeed;
	}
	public void setGr_avspeed(int gr_avspeed) {
		this.gr_avspeed = gr_avspeed;
	}
	
}
